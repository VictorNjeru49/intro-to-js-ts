 // Initial values
 let x = 10;
 let y = 5;
 // Simple assignment
 x = y;  // Assign the value of y to x
 console.log(`x = y: ${x}`);  // Output: x = y: 5
 // Add and assign
 x += y;  // x = x + y
 console.log(`x += y: ${x}`);  // Output: x += y: 10
 // Subtract and assign
 x -= y;  // x = x - y
 console.log(`x -= y: ${x}`);  // Output: x -= y: 5
 // Multiply and assign
 x *= y;  // x = x * y
 console.log(`x *= y: ${x}`);  // Output: x *= y: 25
 // Divide and assign
 x /= y;  // x = x / y
 console.log(`x /= y: ${x}`);  // Output: x /= y: 5
 // Modulus and assign
 x %= y;  // x = x % y
 console.log(`x %= y: ${x}`);  // Output: x %= y: 0
 // Exponentiation and assign (ES2016)
 x **= y;  // x = x ** y
 console.log(`x **= y: ${x}`);  // Output: x **= y: 0
